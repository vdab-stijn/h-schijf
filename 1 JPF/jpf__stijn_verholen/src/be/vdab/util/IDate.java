package be.vdab.util;

public interface IDate {
	
	/**
	 * Returns the day of the current date implementation.
	 * @return the day of the current date.
	 */
	int getDay();
	/**
	 * Returns the month of the current date implementation.
	 * @return the month of the current date.
	 */
	int getMonth();
	/**
	 * Returns the year of the current date implementation.
	 * @return the year of the current date.
	 */
	int getYear();
}
